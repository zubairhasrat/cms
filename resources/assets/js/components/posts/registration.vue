<template>
  <div>
      <div class="container">
        <div class="row">
            <div class="col-md-8 col-md-offset-2">
                <div class="panel panel-default">
                        <div class="panel-heading">Register</div>

                            <div class="panel-body">
                                <form class="form-horizontal" @submit.prevent="registerUser">
                                    <div class="form-group">
                                        <label for="name" class="col-md-4 control-label">Name</label>

                                        <div class="col-md-6">
                                            <input id="name" type="text" class="form-control" name="name" v-model="register.name" required autofocus>
                                        </div>
                                    </div>

                                    <div class="form-group">
                                        <label for="email" class="col-md-4 control-label">E-Mail Address</label>

                                        <div class="col-md-6">
                                            <input id="email" type="email" class="form-control" name="email" v-model="register.email" required>
                                        </div>
                                    </div>

                                    <div class="form-group">
                                        <label for="password" class="col-md-4 control-label">Password</label>

                                        <div class="col-md-6">
                                            <input id="password" type="password" class="form-control" v-model="register.password" name="password" required>
                                        </div>
                                    </div>

                                    <div class="form-group">
                                        <div class="col-md-6 col-md-offset-4">
                                            <button type="submit" class="btn btn-primary">
                                                Register
                                            </button>
                                        </div>
                                    </div>
                                </form>
                            </div>
                        </div>
            </div>
        </div>
    </div>
  </div>
</template>
<script>
export default {
  data(){
      return{
          register:{
              name: '',
              email: '',
              password: ''
          }
      }
  },
  methods:{
    registerUser(){
        axios.post('/register',this.register)
        .then(response => {
            this.$router.push('/registration') 
        })
        .catch(error => {
            console.log(error.response)
        })
    }
  }
}
</script>
